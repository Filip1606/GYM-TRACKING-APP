    private boolean validate(String naziv, String maxTezina) {
        boolean signal = true;

        if (naziv.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Polje naziv ne sme da bude prazno", "Greška", JOptionPane.ERROR_MESSAGE);
            signal = false;
        }
        if (maxTezina.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Polje maksimalna težina ne sme da bude prazno", "Greška", JOptionPane.ERROR_MESSAGE);
            signal = false;
        }

        if (!naziv.isEmpty()) {
            if (naziv.length() > 30) {
                JOptionPane.showMessageDialog(this, "Polje naziv ne sme biti duze od 30 karaktera", "Greška", JOptionPane.ERROR_MESSAGE);
                signal = false;
            }
        }

        if (!maxTezina.isEmpty()) {
            if (maxTezina.matches("[a-zA-Z]+")) {
                JOptionPane.showMessageDialog(this, "Maksimalna težina mora biti broj", "Greška", JOptionPane.ERROR_MESSAGE);
                signal = false;
            }
        }

        return signal;
    }
    
     private void napuniCmb() {
        cmbSprava.removeAllItems();
        try {
            List<Sprava> sprave = Controller.getInstance().vratiSprave();
            for (Sprava sprava : sprave) {
                cmbSprava.addItem(sprava);
            }
            cmbSprava.setSelectedIndex(-1);
        } catch (Exception ex) {
            Logger.getLogger(FrmIzmeniVezbu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
