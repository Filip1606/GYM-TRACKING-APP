    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        String naziv = txtNaziv.getText();
        String maxTezina = txtMaxTezina.getText();

        if (validate(naziv, maxTezina)) {
            try {
                Sprava sprava = new Sprava();
                sprava.setNaziv(naziv);
                sprava.setMaxTezina(Double.parseDouble(maxTezina));
                Controller.getInstance().kreirajSpravu(sprava);
                napuniCmb();
                JOptionPane.showMessageDialog(this, "Sistem je zapamtio novu spravu", "Kreiranje sprave", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti novu spravu", "Kreiranje sprave", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

